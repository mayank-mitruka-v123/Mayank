# 6) Python program to demonstrate Removal of elements in a List
# Creating a List
List=['S','A','V','E','E','T','H','A','S']
print("Initial List :",List)

# Print elements of a range using slice operation
sliced_list=List[3:8]
print(sliced_list)

#Print elements from a pre-defined point to end
slice_list=List[5:]
print(slice_list)

#Print elements from beginning till end
sliced_list=List[:]
print(sliced_list)
